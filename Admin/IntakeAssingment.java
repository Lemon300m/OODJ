    private void fillcombox(){
        String relativePath2 = "Intakes.txt"; //
        String filePath = fnc.getFileLocation(relativePath2);
        
        jComboBox2.removeAllItems();
        try 
        {
            BufferedReader br = new BufferedReader(new FileReader(filePath));
            String line;
            
            while ((line = br.readLine()) != null) {
            jComboBox2.addItem(line); // Add each line from the file to the JComboBox
            }   
        
        }
        catch (IOException ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error reading from the file.");
        }
    }
    
    
    

