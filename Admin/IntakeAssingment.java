    private void table_load()
    {
        try{
        String relativePath = "Student.txt"; // Replace with actual relative path if different
        String filePath = fnc.getFileLocation(relativePath);
        
        File file = new File(filePath);
        BufferedReader br = new BufferedReader(new FileReader(file));
        
        String firstLine = br.readLine().trim();
        String[] columnsName = firstLine.split(",");
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        model.setColumnIdentifiers(columnsName);
        
         model.setRowCount(0);
    
        String line;
        while ((line = br.readLine()) != null) {
            String[] dataRow = line.split(" @ ");
            if (dataRow.length == 3) {
                model.addRow(dataRow);
            } else {
                System.out.println("Invalid line format: " + line);
            }
        }

        br.close();
        }
        catch (IOException ex)
        {
        ex.printStackTrace();
        }
    }
    
    
    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int selectedRow = jTable1.getSelectedRow();
        
        String relativePath2 = "Student.txt";
        String filePath2 = System.getProperty("user.dir") + File.separator + relativePath2;
        
      

        String updatedIntake = (String)jComboBox2.getSelectedItem();
        String tpNumber  = (String) model.getValueAt(selectedRow, 0);

        
        List<String> fileContent = new ArrayList<>();

        // Read file into memory
        try (BufferedReader reader = new BufferedReader(new FileReader(filePath2))) {
            String line;
            while ((line = reader.readLine()) != null) {
                fileContent.add(line);
            }
        } catch (IOException e) {
            e.printStackTrace();
            return;
        }

        // Update the specific line
        boolean updated = false;
        for (int i = 0; i < fileContent.size(); i++) {
            String[] components = fileContent.get(i).split(" @ ");
            if (components[0].trim().equals(tpNumber.trim())) {
                components[2] = updatedIntake;
                fileContent.set(i, String.join(" @ ", components));
                updated = true;
                break;
            }
        }

        if (!updated) {
            System.out.println("TP number not found in file.");
            return;
        }

        // Write updated content back to file
        try (BufferedWriter writer = new BufferedWriter(new FileWriter(filePath2))) {
            for (String line : fileContent) {
                writer.write(line);
                writer.newLine();
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
        
        model.setValueAt(updatedIntake, selectedRow, 2); // Update the model directly
        model.fireTableDataChanged(); // Refresh table data (optional)
        
    }//GEN-LAST:event_jButton5ActionPerformed

